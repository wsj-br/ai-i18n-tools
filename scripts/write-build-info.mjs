#!/usr/bin/env node
/**
 * Writes compile-time build metadata consumed by the CLI (see src/build-info.generated.ts).
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const outPath = path.join(root, "src", "build-info.generated.ts");
const iso = new Date().toISOString();
const body = `/** Auto-generated by scripts/write-build-info.mjs — do not edit */
export const BUILD_TIMESTAMP_ISO = ${JSON.stringify(iso)};
`;
fs.writeFileSync(outPath, body, "utf8");
